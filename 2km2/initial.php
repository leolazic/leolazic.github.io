				<p>On Rhetoric and Symmetry</p>
				
				<p>
					From "Rhetoric and Symmetry: A Neglected Linkage" by Jose Luis
					Caivano
				</p>
				<p>
					Rhetorical figures can be defined as a deviation, a conceptual or
					formal transgression produced in a statement with the aim of
					bringing the receptor of the message to a meaning that is beyond
					the literal meaning. Usually, it is considered that the field of
					rhetoric is poetics, or the figurative language; however, these
					operations are extended across all kinds of discourses and
					languages. For rhetorical operations to be perceived as such, it is
					necessary to establish the existence of rules or norms of
					enunciation, from which rhetorical operations are constituted
					through creative alterations or contraventions.
				</p>
				<p>
					Rhetorical operations also appear in visual images, insofar as the
					observer possess incorporated rules or norms that can be
					transformed or broken to produce a message with a non-conventional
					meaning. Artistic images, painting, architecture, photography,
					caricature, advertisement, and various other genres of visual
					production rely mostly in the rhetorical use of visual signs.
					Rhetorical figures are generally produced on the basis of symmetry
					rules. Sometimes, these rules constitute the rhetorical operations
					themselves; in other cases, they act as a kind of zero degree,
					against which the rhetorical operations, being a transformation or
					a breaking of those rules, can be perceived.
				</p>
				<p>
					…Even when the ancient rhetoric was conceived as the art of
					persuasion by means of the attractive speech, we can also note that
					the beauty of poetry basically relies on the use of rhetoric
					figures: rhyme, metaphor, comparison, antithesis, etc. Already here
					we can postulate a direct relation to the visual arts, because the
					use of symmetry with all its variations in art, architecture, and
					music follows the same purpose: to attract the senses, to create a
					beautiful visual "discourse".
				</p>
				<p>
					…..On the other hand, the concept of dissymmetry, as an imperfect
					symmetry, with little deviations from perfect geometric symmetry,
					does not seem very far from the concept of rhetoric. Dissymmetry
					results are more attractive than those of perfect symmetry.
					According to Pasteur, the Universe is a dissymmetric system and
					life is a function of this dissymmetry.
				</p>
